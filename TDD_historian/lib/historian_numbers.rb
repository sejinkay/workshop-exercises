def roman_numbers(number)
  case
    when number == 1
    "I"
    when number == 2
    "II"
    when number == 3
    "III"
    when number == 4
    "IV"
    when number == 5
    "V"
    when number == 6
    "VI"
    when number == 7
    "VII"
    when number == 8
    "VIII"
    when number == 9
    "IX"
    when number == 10
    "X"
  end
end
